package tlu.finalproject.hrmanagement.model;

public enum ContractType {
    PROBATION,         // Hợp đồng thử việc
    FIXED_TERM,        // Hợp đồng xác định thời hạn
    UNLIMITED_TERM,    // Hợp đồng không xác định thời hạn
    SEASONAL,          // Hợp đồng thời vụ/ngắn hạn
    PART_TIME,         // Hợp đồng bán thời gian
    INTERNSHIP,        // Hợp đồng thực tập
    FREELANCE,         // Hợp đồng khoán việc/tự do
    SERVICE            // Hợp đồng dịch vụ (ngoài biên chế)
}